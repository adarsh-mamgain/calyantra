export default function AppleEmoji() {
  const [opened, setOpen] = React.useState([]);

  const copyEmote = (event, index) => {
    let text = event.target.childNodes[0].data;
    navigator.clipboard.writeText(text);
    let copy = [...opened];
    copy[index] = true;
    setOpen(copy);
    setTimeout(() => {
      let original = [...opened]
      original[index] = false;
      setOpen(original);
    }, 1000);
  };

  return (
    <Box sx={{ px: 20, m: 3 }}>
      <Crumbs />
      <Grid container justifyContent="center">
        {emoticons.map(
          (options, index) => (
            opened.push(false),
            (
              <div>
                <Tooltip
                  open={opened[index]}
                  disableFocusListener
                  disableHoverListener
                  disableTouchListener
                  placement="top"
                  title="Copied!"
                >
                  <Button key={index} sx={{ fontSize: 40 }} onClick={(e) => copyEmote(e, index)}>
                    {options.emoji}
                  </Button>
                </Tooltip>
              </div>
            )
          )
        )}
      </Grid>
    </Box>
  );
}
